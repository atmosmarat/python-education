#вывод переменных в столбик
a = 1
b = 2 
c = 3 
print (a, b, c, sep="\n")
#sep - разделитель строки в данном случае со значением переноса на новую строку

#вывод переменных в строку 
a = 1
b = 2 
c = 3 
print (a, b, c, end =" ")
#end - завершающий символ или строка, в данном случае переопределяет стандартный вывод со значения "на следующую строку" на значение "через пробел"